package business;

/**
 *
 * @author aluno
 */
public class Usuario {
    private String nome;
    private String email;
    private int Idade;
    private String login;
    private String senha;

    public Usuario(String nome, String email, int Idade, String login, String senha) {
        this.nome = nome;
        this.email = email;
        this.Idade = Idade;
        this.login = login;
        this.senha = senha;
    }
    
    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getIdade() {
        return Idade;
    }

    public void setIdade(int Idade) {
        this.Idade = Idade;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
    
}
